package gcp.springmvc.bean;

public class FrequencyBean {
	private String DM;
	private String DMMC;
	private String LDW;
	public String getDM() {
		return DM;
	}
	public void setDM(String dM) {
		DM = dM;
	}
	public String getDMMC() {
		return DMMC;
	}
	public void setDMMC(String dMMC) {
		DMMC = dMMC;
	}
	public String getLDW() {
		return LDW;
	}
	public void setLDW(String lDW) {
		LDW = lDW;
	}		
}
